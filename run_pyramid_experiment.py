#!/usr/bin/env python3
"""Simple Pyramid index experiment script.

Flow:
1) Connect to PostgreSQL (hardcoded params below)
2) Create extension/table
3) Insert synthetic data via pyramid_generate extension
4) Build Pyramid expression index
5) Run the Pyramid range query and print matching IDs
"""

from __future__ import annotations
import argparse
from typing import Any, List, Sequence, Tuple
import time

import psycopg2

DB_CONFIG = {
    "host": "127.0.0.1",
    "port": 5432,
    "dbname": "pg_pyramid",
    "user": "postgres",
    "password": "27122005",
}

TABLE_NAME = "points"
INDEX_NAME = f"{TABLE_NAME}_pyramid_idx"

#1M objects
ROW_COUNT = 1000000

PYRAMID_QUERY_SQL = f"""
WITH q AS (
    SELECT %s::float8[] AS lo,
           %s::float8[] AS hi
), r AS (
    SELECT pr.range_lo, pr.range_hi
    FROM q, LATERAL pyramid_ranges(q.lo, q.hi) pr
)
SELECT p.id
FROM {TABLE_NAME} p, q, r
WHERE pyramid_value(p.v) BETWEEN r.range_lo AND r.range_hi
  AND pyramid_contains(p.v, q.lo, q.hi)
GROUP BY p.id
ORDER BY p.id
"""

def ensure_extensions(cur: psycopg2.extensions.cursor) -> None:
    """Ensure required extensions are installed in the current database."""
    cur.execute("CREATE EXTENSION IF NOT EXISTS pyramid;")
    cur.execute("CREATE EXTENSION IF NOT EXISTS pyramid_generate;")

def ensure_table(cur: psycopg2.extensions.cursor, dims: int) -> None:
    """Create the experiment table if it does not already exist."""
    cur.execute(
        f"""
        CREATE TABLE IF NOT EXISTS {TABLE_NAME} (
            id BIGSERIAL PRIMARY KEY,
            v  FLOAT8[] NOT NULL
        );
        """
    )

def clear_table(cur: psycopg2.extensions.cursor) -> None:
    """Delete all rows from the experiment table. Drop the Pyramid expression index if it exists."""
    cur.execute(f"DELETE FROM {TABLE_NAME};")
    cur.execute(f"DROP INDEX IF EXISTS {INDEX_NAME};")

def insert_vectors(cur: psycopg2.extensions.cursor, rows: int, dims: int) -> None:
    """Insert random vectors generated by the pyramid_generate extension."""
    cur.execute(
        f"""
        INSERT INTO {TABLE_NAME}(v)
        SELECT v
        FROM pyramid_generate(%s, %s::bigint) AS t(v);
        """,
        (dims, rows),
    )

def create_and_analyze_index(cur: psycopg2.extensions.cursor) -> None:
    """Create the Pyramid expression index and update planner statistics."""
    cur.execute(
        f"""
        CREATE INDEX {INDEX_NAME}
        ON {TABLE_NAME} USING btree (pyramid_value(v));
        """
    )
    cur.execute(f"ANALYZE {TABLE_NAME};")
    
def run_query(cur: psycopg2.extensions.cursor, lo: Sequence[float], hi: Sequence[float]) -> Tuple[List[int], float]:
    """Run the Pyramid candidate+refinement query and return matching IDs and elapsed time (seconds).

    This measures wall-clock time for the execute+fetchall call so a single
    execution returns both the result set and the time taken.
    """
    t0 = time.perf_counter()
    cur.execute(PYRAMID_QUERY_SQL, (list(lo), list(hi)))
    rows = [row[0] for row in cur.fetchall()]
    t1 = time.perf_counter()
    return rows, t1 - t0

def is_idx_used(cur: psycopg2.extensions.cursor, lo: Sequence[float], hi: Sequence[float]) -> bool:
    """Return True if EXPLAIN plan uses the Pyramid index for the query."""

    def plan_uses_index(node: dict[str, Any]) -> bool:
        """Recursively check if the plan node or any child node uses the Pyramid index."""
        node_type = node.get("Node Type", "")
        index_name = node.get("Index Name", "")

        if (
            node_type in {"Index Scan", "Index Only Scan", "Bitmap Index Scan"}
            and index_name == INDEX_NAME
        ):
            return True

        for child in node.get("Plans", []):
            if plan_uses_index(child):
                return True

        return False

    explain_sql = f"EXPLAIN (FORMAT JSON) {PYRAMID_QUERY_SQL}"
    cur.execute(explain_sql, (list(lo), list(hi)))
    explain_result = cur.fetchone()

    if not explain_result:
        return False

    # PostgreSQL returns EXPLAIN (FORMAT JSON) as a one-row, one-column value
    # containing a list with the top-level plan document.
    explain_doc = explain_result[0]
    root_plan = explain_doc[0].get("Plan", {}) if explain_doc else {}
    return plan_uses_index(root_plan)

parser = argparse.ArgumentParser(
        prog="run_pyramid_experiment.py",
        description="Run Pyramid experiment (specify dimensions via CLI).",
    )
parser.add_argument("-d", "--dims", type=int, default=8, help="vector dimensionality")
args = parser.parse_args()
dims = args.dims

with psycopg2.connect(**DB_CONFIG) as conn:
    with conn.cursor() as cur:
        ensure_extensions(cur)
        ensure_table(cur, dims)

        print("Starting with a clean table and no index...")
        clear_table(cur)

        print(f"Inserting {ROW_COUNT:,} vectors of dimension {dims} using pyramid_generate...")
        insert_vectors(cur, ROW_COUNT, dims)

        query_lo = [0.1] * dims
        query_hi = [0.5] * dims

        matched_ids_no_idx, time_no_idx = run_query(cur, query_lo, query_hi)
        print(f"Total matches without index: {len(matched_ids_no_idx)}")
        print(f"Query time without index: {time_no_idx:.6f} s")

        print("Creating Pyramid expression index and updating planner statistics...")
        create_and_analyze_index(cur)

        if (not is_idx_used(cur, query_lo, query_hi)):
            print("WARNING: Pyramid index is not being used for the query plan! Fix the selectivity!")
        else:
            matched_ids_with_idx, time_with_idx = run_query(cur, query_lo, query_hi)
            print(f"Total matches with index: {len(matched_ids_with_idx)}")
            print(f"Query time with index: {time_with_idx:.6f} s")

            if matched_ids_with_idx == matched_ids_no_idx:
                print("Result sets are equal.")
            else:
                print("Result sets are not equal.")
